public class TransaksiPengisian {
    kendaraan kendaraan;
    BBM bbm;
    double liter;
    double totalBayar;

    public TransaksiPengisian(kendaraan kendaraan, BBM bbm, double liter, double totalBayar) {
        this.kendaraan = kendaraan;
        this.bbm = bbm;
        this.liter = liter;
        this.totalBayar = totalBayar;
    }

}
